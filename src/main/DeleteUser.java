    private void EIdKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_EIdKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode()==KeyEvent.VK_ENTER){
            try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db","root","");
            String sql = "select * from empdetail where EmpId = ?";
            ResultSet rs = null;
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, EId.getText());
            rs = pst.executeQuery();
        if (rs.next())
            {
            Ename.setText(rs.getString(2));
            Edes.setText(rs.getString(3));
            Esalary.setText(rs.getString(4));
            Ephone.setText(rs.getString(5));
            EAddhar.setText(rs.getString(6));
            Eaddress.setText(rs.getString(7));
            Eage.setText(rs.getString(8));
            }
                }
            
            
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
            
    }//GEN-LAST:event_EIdKeyPressed
        }
            }
