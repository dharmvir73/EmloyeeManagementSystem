        // TODO add your handling code here:
        String v1 = EId.getText();
        int p = JOptionPane.showConfirmDialog(null,"Are You Sure to Delete Data ?", "Delete Record", JOptionPane.YES_NO_OPTION);
        if (p == 0){
            try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db","root","");
            String sql = "DELETE FROM empdetail WHERE EmpId = '"+v1+"'";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.execute();
            
            
            String sql2 = "DELETE FROM salary WHERE EmpId = '"+v1+"'";
            PreparedStatement pst2 = con.prepareStatement(sql2);
            pst2.execute();
            
            JOptionPane.showMessageDialog(null,"Data Deleted Succesfully");
           
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        }
